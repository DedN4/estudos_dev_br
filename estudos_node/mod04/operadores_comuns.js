const a = 7
let b = 3

b = a // Atribuindo o valor de a no lugar de b
console.log(b)

b += a // b = b + a - Soma
console.log(b)

b -= a // b = b - a - Subtração
console.log(b)

b *= a // b = b * a - Multiplicação
console.log(b)

b /= a // b = b / a - Divisão
console.log(b)

b %= a // b = b % a - Resto da divisão 
console.log(b)
