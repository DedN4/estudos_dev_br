let contador = 1

// Enquando a condição 'while' for verdadeira ele não irá parar de executar.

while (contador <= 10) {
    console.log(`Contagem em ${contador}`)
    contador++
}


for (let num = 1; num <= 10; num++) {
    console.log(num)   
}

// 'For' trás bastante vantagem para usarmos em situações no qual precisamos de um limite, ele tem esse foco e é mais simples para isso

// Não significa que somente isso podemos fazer.