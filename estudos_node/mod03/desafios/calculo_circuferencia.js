// Calculo de Raio
const PI = 3.14 // Valor de PI é uma constante matemática 
const raio = 10 // Valor do Raio é variável
calcRaio = (raio * raio) * PI // PI * Raio * Raio
console.log("O valor da área é de: " + calcRaio + "m²")

areaCirc = Math.PI * raio * raio; // Math.PI - Exibir o valor de PIO segundo a matematica
console.log("O valor da área é de: " + areaCirc + "m²")
